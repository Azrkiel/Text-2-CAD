# 6. CLI
# ---------------------------------------------------------------------------

def main():
    import argparse

    parser = argparse.ArgumentParser(
        description="RLAIF Machinist training — T3-04"
    )
    subparsers = parser.add_subparsers(dest="command")

    train_p = subparsers.add_parser("train", help="Run RLAIF training")
    train_p.add_argument("--sft-model", required=True, help="SFT fine-tuned Machinist path")
    train_p.add_argument("--output", default="checkpoints/rlaif_machinist")
    train_p.add_argument("--telemetry", default=None, help="Telemetry JSONL for prompts")
    train_p.add_argument("--steps", type=int, default=10_000)
    train_p.add_argument("--batch-size", type=int, default=64)
    train_p.add_argument("--no-preference-reward", action="store_true")
    train_p.add_argument("--gemini-api-key", default=None)
    train_p.add_argument("--no-4bit", action="store_true")

    eval_p = subparsers.add_parser("eval", help="Evaluate RLAIF vs SFT")
    eval_p.add_argument("--rlaif-model", required=True)
    eval_p.add_argument("--sft-model", required=True)
    eval_p.add_argument("--telemetry", required=True)
    eval_p.add_argument("--n-samples", type=int, default=200)

    macro_p = subparsers.add_parser("macro-test", help="Test macro-action segmentation")
    macro_p.add_argument("--script", required=True, help="CadQuery script file")

    args = parser.parse_args()
    logging.basicConfig(level=logging.INFO)

    if args.command == "train":
        config = RLAIFConfig(
            sft_model_path=args.sft_model,
            output_dir=args.output,
            n_steps=args.steps,
            batch_size=args.batch_size,
            use_preference_reward=not args.no_preference_reward,
            gemini_api_key=args.gemini_api_key,
            load_in_4bit=not args.no_4bit,
        )

        prompts = load_prompt_dataset(
            telemetry_jsonl=args.telemetry,
        )
        if len(prompts) < 10:
            print("Warning: very few training prompts found. Add --telemetry for more.")

        from backend.strategies import get_strategy  # type: ignore
        system_prompt = get_strategy("A", "")  # Default Domain A strategy

        trainer = RLAIFMachinist(config)
        result = trainer.train(prompts, system_prompt)
        print(json.dumps(result, indent=2))

    elif args.command == "eval":
        prompts = load_prompt_dataset(telemetry_jsonl=args.telemetry)
        config = RLAIFConfig(sft_model_path=args.rlaif_model)
        trainer = RLAIFMachinist(config)
        result = trainer.evaluate_vs_sft(
            eval_prompts=prompts,
            sft_model_path=args.sft_model,
            n_samples=args.n_samples,
        )
        print(json.dumps(result, indent=2))

    elif args.command == "macro-test":
        with open(args.script) as f:
            code = f.read()
        actions = segment_into_macro_actions(code)
        print(f"Found {len(actions)} macro-actions:")
        for i, action in enumerate(actions):
            print(f"  [{i}] line {action.start_line}: {action.method_name}")

    else:
        parser.print_help()


if __name__ == "__main__":
    main()
