def load_prompt_dataset(
    telemetry_jsonl: Optional[str] = None,
    seed_prompts: Optional[list[str]] = None,
    max_prompts: int = 10_000,
) -> list[str]:
    """Load prompts for RLAIF online rollout.

    Prioritizes:
    1. Telemetry records with failed compilations (hard negatives — highest value)
    2. Telemetry records with high LVM scores (good diversity)
    3. Seed prompts (static baseline)

    Args:
        telemetry_jsonl: Path to telemetry JSONL from telemetry.py.
        seed_prompts: Static prompt list as fallback.
        max_prompts: Maximum number of prompts to load.

    Returns:
        Shuffled list of prompt strings.
    """
    import random

    prompts = []

    if telemetry_jsonl and Path(telemetry_jsonl).exists():
        failed_prompts = []
        success_prompts = []

        with open(telemetry_jsonl) as f:
            for line in f:
                try:
                    record = json.loads(line.strip())
                    prompt = record.get("prompt", record.get("part_description", ""))
                    if not prompt:
                        continue
                    if record.get("success", True) is False:
                        failed_prompts.append(prompt)
                    else:
                        success_prompts.append(prompt)
                except Exception:  # noqa: BLE001
                    pass

        # Hard negatives first (more training signal)
        prompts.extend(failed_prompts)
        prompts.extend(success_prompts)
        logger.info(
            "Loaded %d prompts from telemetry (%d failed, %d succeeded)",
            len(prompts), len(failed_prompts), len(success_prompts),
        )

    if seed_prompts:
        prompts.extend(seed_prompts)

    if not prompts:
        # Minimal default prompts to allow the pipeline to run
        prompts = [
            "A cylindrical housing 60mm OD, 50mm ID, 100mm tall with 4 M5 bolt holes on a 70mm bolt circle",
            "An L-shaped bracket 80mm x 60mm with 3mm wall thickness and 2 mounting holes",
            "A spur gear with 24 teeth, module 2, 20° pressure angle, 20mm bore",
            "A wing section with NACA 2412 profile, 150mm chord, 50mm span, 2mm skin thickness",
            "A flanged shaft 20mm diameter, 100mm long with 5mm wide keyway, M6 thread on end",
        ]

    random.shuffle(prompts)
    return prompts[:max_prompts]


