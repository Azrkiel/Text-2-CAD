EXECUTION_REWARD_SUCCESS = 1.0
EXECUTION_REWARD_FAILURE = -1.0
PREFERENCE_REWARD_MAX = 1.0
PREFERENCE_REWARD_MIN = -1.0

HYBRID_EXECUTION_WEIGHT = 0.7
HYBRID_PREFERENCE_WEIGHT = 0.3

# Gemini model for preference scoring
PREFERENCE_MODEL = "gemini-2.5-flash"


def compute_execution_reward(script: str, timeout: int = 30) -> float:
    """Compute binary execution reward by compiling CadQuery script.

    Reuses the same compilation approach as telemetry.py / the Critic Loop.
    No new infrastructure required — existing OCCT environment is used.

    Args:
        script: CadQuery Python script generated by the Machinist.
        timeout: Compilation timeout in seconds.

    Returns:
        1.0 if script compiles to a valid solid, -1.0 otherwise.
    """
    with tempfile.NamedTemporaryFile(mode="w", suffix=".py", delete=False) as f:
        f.write(script)
        tmp_path = f.name

    try:
        check_script = textwrap.dedent(f"""
            import sys
            sys.path.insert(0, '/app')
            exec(open({repr(tmp_path)}).read())
            # Verify 'result' is a non-empty solid
            if 'result' not in dir():
                print("NO_RESULT")
                sys.exit(1)
            import cadquery as cq
            if isinstance(result, cq.Assembly):
                shape = result.toCompound()
            elif isinstance(result, cq.Workplane):
                shape = result.val()
            else:
                print("INVALID_TYPE")
                sys.exit(1)
            from OCC.Core.BRepCheck import BRepCheck_Analyzer
            analyzer = BRepCheck_Analyzer(shape.wrapped)
            if not analyzer.IsValid():
                print("INVALID_SOLID")
                sys.exit(1)
            print("SUCCESS")
        """)

        with tempfile.NamedTemporaryFile(mode="w", suffix=".py", delete=False) as check_f:
            check_f.write(check_script)
            check_path = check_f.name

        result = subprocess.run(
            ["python", check_path],
            capture_output=True,
            text=True,
            timeout=timeout,
        )
        success = "SUCCESS" in result.stdout
        return EXECUTION_REWARD_SUCCESS if success else EXECUTION_REWARD_FAILURE

    except subprocess.TimeoutExpired:
        logger.debug("Execution reward: timeout after %ds", timeout)
        return EXECUTION_REWARD_FAILURE
    except Exception as exc:  # noqa: BLE001
        logger.debug("Execution reward error: %s", exc)
        return EXECUTION_REWARD_FAILURE
    finally:
        for p in [tmp_path, check_path]:
            try:
                os.unlink(p)
            except OSError:
                pass


async def compute_preference_reward(
    script: str,
    prompt: str,
    rendered_image_path: Optional[str] = None,
    api_key: Optional[str] = None,
) -> float:
    """Compute AI preference reward using Gemini.

    Asks Gemini: "On a scale of 1–5, how well does this 3D model match
    the description?" Normalizes to [-1, 1].

    Args:
        script: The CadQuery script (used if image not available).
        prompt: Original design prompt.
        rendered_image_path: Optional path to rendered GLB screenshot.
            If None, evaluates based on code quality inspection.
        api_key: Gemini API key.

    Returns:
        Preference reward in [-1, 1].
    """
    import google.generativeai as genai  # type: ignore

    if api_key:
        genai.configure(api_key=api_key)

    model = genai.GenerativeModel(
        model_name=PREFERENCE_MODEL,
        system_instruction=(
            "You are a mechanical engineering quality reviewer. "
            "Rate how well a CadQuery script implements a given design brief. "
            "Respond with ONLY a JSON object: {\"score\": <integer 1-5>, \"reason\": \"<brief>\"}. "
            "Score 1: completely wrong geometry, Score 3: mostly correct with minor issues, "
            "Score 5: perfectly matches the brief with clean geometry."
        ),
    )

    prompt_text = (
        f"Design brief: {prompt}\n\n"
        f"CadQuery script:\n```python\n{script[:2000]}\n```\n\n"
        "Rate how well this script implements the design brief (1-5)."
    )

    try:
        response = await model.generate_content_async(prompt_text)
        data = json.loads(response.text)
        score = float(data.get("score", 3))
        # Normalize [1, 5] → [-1, 1]
        normalized = (score - 3.0) / 2.0
        return float(normalized)
    except Exception as exc:  # noqa: BLE001
        logger.debug("Preference reward API error: %s", exc)
        return 0.0  # neutral on error


def compute_hybrid_reward(
    execution_reward: float,
    preference_reward: float,
    w_exec: float = HYBRID_EXECUTION_WEIGHT,
    w_pref: float = HYBRID_PREFERENCE_WEIGHT,
) -> float:
    """Combine execution and preference rewards.

    Args:
        execution_reward: Binary reward from OCCT compilation.
        preference_reward: AI preference score in [-1, 1].
        w_exec: Weight for execution reward (default 0.7).
        w_pref: Weight for preference reward (default 0.3).

    Returns:
        Hybrid reward in [-1, 1] (approximately).
    """
    return w_exec * execution_reward + w_pref * preference_reward


