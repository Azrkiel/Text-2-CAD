class RLAIFMachinist:
    """PPO trainer for RLAIF fine-tuning of the Machinist model.

    Wraps HuggingFace trl's PPOTrainer with:
    - Hybrid reward (execution + AI preference)
    - Macro-action credit assignment
    - KL divergence monitoring against SFT reference model
    """

    def __init__(self, config: RLAIFConfig):
        self.config = config
        self._trainer = None
        self._model = None
        self._ref_model = None
        self._tokenizer = None

    def _initialize(self):
        """Lazy initialization — avoids import errors if trl not installed."""
        try:
            import torch
            from peft import LoraConfig, get_peft_model  # type: ignore
            from transformers import AutoModelForCausalLM, AutoTokenizer  # type: ignore
            from trl import PPOConfig, PPOTrainer  # type: ignore

            logger.info("Initializing RLAIF trainer from %s", self.config.sft_model_path)

            # Tokenizer
            self._tokenizer = AutoTokenizer.from_pretrained(
                self.config.sft_model_path,
                padding_side="left",
            )
            if self._tokenizer.pad_token is None:
                self._tokenizer.pad_token = self._tokenizer.eos_token

            # Active model (will be updated by PPO)
            load_kwargs = {}
            if self.config.load_in_4bit:
                from transformers import BitsAndBytesConfig  # type: ignore
                load_kwargs["quantization_config"] = BitsAndBytesConfig(
                    load_in_4bit=True,
                    bnb_4bit_quant_type="nf4",
                    bnb_4bit_compute_dtype=torch.float16,
                )
            load_kwargs["device_map"] = self.config.device

            self._model = AutoModelForCausalLM.from_pretrained(
                self.config.sft_model_path,
                **load_kwargs,
            )

            # Reference model (frozen SFT — used for KL penalty)
            self._ref_model = AutoModelForCausalLM.from_pretrained(
                self.config.sft_model_path,
                **load_kwargs,
            )
            for param in self._ref_model.parameters():
                param.requires_grad = False

            # PPO config
            ppo_config = PPOConfig(
                model_name=self.config.sft_model_path,
                learning_rate=self.config.learning_rate,
                batch_size=self.config.batch_size,
                mini_batch_size=self.config.mini_batch_size,
                gradient_accumulation_steps=self.config.gradient_accumulation_steps,
                ppo_epochs=self.config.ppo_epochs,
                kl_penalty=self.config.kl_penalty,
                init_kl_coef=self.config.init_kl_coeff,
                target=self.config.target_kl,
                adap_kl_ctrl=self.config.adaptive_kl_ctrl,
                log_with="tensorboard",
                project_kwargs={"logging_dir": os.path.join(self.config.output_dir, "tb_logs")},
            )

            # PPO trainer
            self._trainer = PPOTrainer(
                config=ppo_config,
                model=self._model,
                ref_model=self._ref_model,
                tokenizer=self._tokenizer,
            )

            logger.info("RLAIF trainer initialized successfully")

        except ImportError as exc:
            raise RuntimeError(
                "trl, transformers, peft required. "
                "Install: pip install trl>=0.7.0 transformers accelerate peft bitsandbytes"
            ) from exc

    def train(
        self,
        prompts: list[str],
        system_prompt: str,
        eval_prompts: Optional[list[str]] = None,
    ) -> dict:
        """Run RLAIF training loop.

        Args:
            prompts: Training prompt strings for online rollout.
            system_prompt: Machinist system prompt (domain strategy).
            eval_prompts: Optional held-out prompts for evaluation.

        Returns:
            Training summary with final invalidity rate and LVM improvement.
        """
        if self._trainer is None:
            self._initialize()

        import asyncio
        import random

        os.makedirs(self.config.output_dir, exist_ok=True)

        step_metrics: list[dict] = []
        kl_history: list[float] = []

        logger.info(
            "Starting RLAIF training: %d prompts, %d steps",
            len(prompts), self.config.n_steps,
        )

        for step in range(self.config.n_steps):
            # Sample batch
            batch_prompts = random.sample(
                prompts, min(self.config.mini_batch_size, len(prompts))
            )

            # Format as chat messages
            formatted = [
                self._tokenizer.apply_chat_template(
                    [
                        {"role": "system", "content": system_prompt},
                        {"role": "user", "content": f"Generate CadQuery code for: {p}"},
                    ],
                    tokenize=False,
                    add_generation_prompt=True,
                )
                for p in batch_prompts
            ]

            # Tokenize
            inputs = self._tokenizer(
                formatted,
                return_tensors="pt",
                padding=True,
                truncation=True,
                max_length=2048,
            )

            # Generate responses
            try:
                response_tensors = self._trainer.generate(
                    inputs["input_ids"].to(self._trainer.accelerator.device),
                    max_new_tokens=self.config.max_new_tokens,
                    temperature=self.config.temperature,
                    top_p=self.config.top_p,
                    do_sample=True,
                )

                # Decode generated scripts
                generated_texts = self._tokenizer.batch_decode(
                    response_tensors, skip_special_tokens=True
                )

                # Compute rewards
                rewards = []
                for prompt, script in zip(batch_prompts, generated_texts):
                    exec_r = compute_execution_reward(script)

                    pref_r = 0.0
                    use_pref = (
                        self.config.use_preference_reward
                        and step % int(1.0 / max(self.config.preference_eval_fraction, 0.01)) == 0
                    )
                    if use_pref:
                        try:
                            pref_r = asyncio.get_event_loop().run_until_complete(
                                compute_preference_reward(
                                    script, prompt,
                                    api_key=self.config.gemini_api_key,
                                )
                            )
                        except Exception as exc:  # noqa: BLE001
                            logger.debug("Preference reward error: %s", exc)

                    hybrid_r = compute_hybrid_reward(exec_r, pref_r)

                    if self.config.use_macro_action_credit:
                        macro_actions = segment_into_macro_actions(script)
                        macro_rewards = assign_macro_action_rewards(
                            macro_actions, hybrid_r, self.config.macro_discount
                        )
                        # Use final macro-action reward as PPO reward
                        # (full MA-RLHF would attribute per token, but this is a
                        # reasonable approximation for the scaffolding)
                        ppo_reward = macro_rewards[-1] if macro_rewards else hybrid_r
                    else:
                        ppo_reward = hybrid_r

                    rewards.append(ppo_reward)

                # PPO update
                import torch
                reward_tensors = [
                    torch.tensor([r], dtype=torch.float32) for r in rewards
                ]
                stats = self._trainer.step(
                    inputs["input_ids"].to(self._trainer.accelerator.device),
                    response_tensors,
                    reward_tensors,
                )

                kl = float(stats.get("objective/kl", 0.0))
                kl_history.append(kl)
                step_metrics.append({
                    "step": step,
                    "mean_reward": float(sum(rewards) / len(rewards)),
                    "kl": kl,
                    "invalidity_rate": sum(1 for r in rewards if r < 0) / len(rewards),
                })

                if step % 50 == 0:
                    logger.info(
                        "Step %d/%d — reward=%.3f, kl=%.3f, invalidity=%.2f",
                        step, self.config.n_steps,
                        step_metrics[-1]["mean_reward"],
                        kl,
                        step_metrics[-1]["invalidity_rate"],
                    )

                # Early stopping if KL diverges
                if kl > self.config.target_kl * 3:
                    logger.warning(
                        "KL divergence %.2f exceeds 3× target (%.2f). "
                        "Stopping to prevent reward hacking.",
                        kl, self.config.target_kl * 3,
                    )
                    break

                # Evaluation checkpoint
                if step > 0 and step % self.config.eval_every == 0:
                    self._save_checkpoint(step)

            except Exception as exc:  # noqa: BLE001
                logger.error("Training step %d failed: %s", step, exc)
                continue

        # Final checkpoint
        self._save_checkpoint("final")

        # Summary
        final_invalidity = (
            sum(m["invalidity_rate"] for m in step_metrics[-100:]) /
            max(len(step_metrics[-100:]), 1)
        )
        mean_kl = sum(kl_history) / max(len(kl_history), 1)

        summary = {
            "steps_completed": len(step_metrics),
            "final_invalidity_rate": final_invalidity,
            "mean_kl": mean_kl,
            "kl_stable": mean_kl < self.config.target_kl,
            "output_dir": self.config.output_dir,
        }

        with open(os.path.join(self.config.output_dir, "rlaif_summary.json"), "w") as f:
            json.dump(summary, f, indent=2)

        logger.info("RLAIF training complete: %s", summary)
        return summary

    def _save_checkpoint(self, label):
        try:
            save_dir = os.path.join(self.config.output_dir, f"checkpoint_{label}")
            self._trainer.save_pretrained(save_dir)
            logger.info("Saved RLAIF checkpoint: %s", save_dir)
        except Exception as exc:  # noqa: BLE001
            logger.warning("Checkpoint save failed: %s", exc)

    def evaluate_vs_sft(
        self,
        eval_prompts: list[str],
        sft_model_path: str,
        n_samples: int = 200,
    ) -> dict:
        """Compare RLAIF model against SFT baseline on invalidity rate and LVM score.

        Args:
            eval_prompts: Held-out evaluation prompts.
            sft_model_path: Path to SFT model for comparison.
            n_samples: Number of prompts to evaluate.

        Returns:
            {rlaif_invalidity, sft_invalidity, improvement, lvm_improvement}
        """
        import random

        sample = random.sample(eval_prompts, min(n_samples, len(eval_prompts)))

        def _eval_model(model_path: str, use_current: bool = False) -> dict:
            if use_current:
                gen_model = self._trainer.model if self._trainer else None
                gen_tokenizer = self._tokenizer
            else:
                try:
                    from transformers import AutoModelForCausalLM, AutoTokenizer  # type: ignore
                    gen_model = AutoModelForCausalLM.from_pretrained(model_path)
                    gen_tokenizer = AutoTokenizer.from_pretrained(model_path)
                except Exception as exc:  # noqa: BLE001
                    return {"error": str(exc), "invalidity_rate": 0.5}

            if gen_model is None:
                return {"error": "No model loaded", "invalidity_rate": 0.5}

            invalid_count = 0
            for prompt in sample:
                try:
                    inputs = gen_tokenizer(
                        prompt, return_tensors="pt", max_length=512, truncation=True
                    )
                    outputs = gen_model.generate(**inputs, max_new_tokens=1024)
                    script = gen_tokenizer.decode(outputs[0], skip_special_tokens=True)
                    exec_r = compute_execution_reward(script)
                    if exec_r < 0:
                        invalid_count += 1
                except Exception:  # noqa: BLE001
                    invalid_count += 1

            return {"invalidity_rate": invalid_count / len(sample)}

        rlaif_results = _eval_model("", use_current=True)
        sft_results = _eval_model(sft_model_path)

        improvement = sft_results["invalidity_rate"] - rlaif_results["invalidity_rate"]
        return {
            "rlaif_invalidity_rate": rlaif_results["invalidity_rate"],
            "sft_invalidity_rate": sft_results["invalidity_rate"],
            "invalidity_improvement": improvement,
            "rlaif_beats_sft": improvement > 0,
            "n_samples": len(sample),
        }


