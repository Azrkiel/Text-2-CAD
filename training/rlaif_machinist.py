# 2. Macro-Action Credit Assignment
# ---------------------------------------------------------------------------

@dataclass
class MacroAction:
    """A single CadQuery method-chain operation."""
    code: str           # The .method(args) string
    start_line: int
    end_line: int
    method_name: str


def segment_into_macro_actions(code: str) -> list[MacroAction]:
    """Split CadQuery script into method-chain macro-actions.

    Each fluent method call (.circle(r), .extrude(h), .constrain(...)) is
    treated as one macro-action for reward attribution purposes. This avoids
    the credit assignment problem of token-level PPO by grouping semantically
    meaningful operations together.

    Args:
        code: CadQuery Python script.

    Returns:
        List of MacroAction objects in script order.

    Implementation note:
        Uses Python's ast module to find all chained Attribute.Call nodes.
        The method chain is decomposed by walking the AST left-to-right.
    """
    macro_actions = []

    try:
        tree = ast.parse(code)
    except SyntaxError:
        # Fallback: split on newlines as poor-man's segmentation
        lines = code.strip().split("\n")
        return [
            MacroAction(
                code=line.strip(),
                start_line=i + 1,
                end_line=i + 1,
                method_name="<unknown>",
            )
            for i, line in enumerate(lines)
            if line.strip() and not line.strip().startswith("#")
        ]

    def extract_method_calls(node) -> list[tuple[str, int, str]]:
        """Recursively extract (code_snippet, line_no, method_name) tuples."""
        results = []
        if isinstance(node, ast.Expr) and isinstance(node.value, ast.Call):
            # Walk chain of attribute calls
            chain = _flatten_method_chain(node.value)
            for method_name, lineno in chain:
                results.append((method_name, lineno, method_name))
        elif isinstance(node, ast.Assign):
            if isinstance(node.value, ast.Call):
                chain = _flatten_method_chain(node.value)
                for method_name, lineno in chain:
                    results.append((method_name, lineno, method_name))
        return results

    def _flatten_method_chain(call_node) -> list[tuple[str, int]]:
        """Flatten a.b().c().d() into [("b", ln), ("c", ln), ("d", ln)]."""
        methods = []
        node = call_node
        while isinstance(node, ast.Call):
            if isinstance(node.func, ast.Attribute):
                methods.append((
                    node.func.attr,
                    getattr(node, "lineno", 0),
                ))
                node = node.func.value
            else:
                break
        methods.reverse()
        return methods

    for stmt in ast.walk(tree):
        calls = extract_method_calls(stmt)
        for method_name, lineno, code_snippet in calls:
            macro_actions.append(MacroAction(
                code=method_name,
                start_line=lineno,
                end_line=lineno,
                method_name=method_name,
            ))

    # If AST walk found nothing useful, fall back to line-based
    if not macro_actions:
        lines = code.strip().split("\n")
        macro_actions = [
            MacroAction(
                code=line.strip(),
                start_line=i + 1,
                end_line=i + 1,
                method_name="<line>",
            )
            for i, line in enumerate(lines)
            if line.strip() and not line.strip().startswith("#")
        ]

    return macro_actions


def assign_macro_action_rewards(
    macro_actions: list[MacroAction],
    terminal_reward: float,
    discount: float = 0.99,
) -> list[float]:
    """Attribute terminal reward to macro-actions via discounted return.

    Uses discounted returns: r_t = terminal_reward * gamma^(T-t)
    where T is the last macro-action and t is the current one.

    Args:
        macro_actions: Segmented macro-actions.
        terminal_reward: Final reward after compilation/evaluation.
        discount: Discount factor gamma.

    Returns:
        List of per-macro-action rewards (same length as macro_actions).
    """
    n = len(macro_actions)
    if n == 0:
        return []

    rewards = []
    for i in range(n):
        t_remaining = n - 1 - i
        r = terminal_reward * (discount ** t_remaining)
        rewards.append(r)

    return rewards


